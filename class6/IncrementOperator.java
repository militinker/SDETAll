package class6;

public class IncrementOperator {
    public static void main(String[] args) {


        int number = 10;
        number=number+1;
        System.out.println(number);
        number+=1;
        System.out.println(number);
        number++; //also incraments the number by 1
        System.out.println(number);

        int number2=10;
        number2--;// decreases the number by 1
        System.out.println(number2);

    }
}
