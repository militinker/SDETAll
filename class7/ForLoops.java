package class7;

public class ForLoops {
    public static void main(String[] args) {
        // print numbers from 0 to 9

        int number=0;

        while (number<10){
            System.out.println(number);
            number++;
        }

        // initiliaze ; condition; increment or decrement
        for(int i=0;i<10;i++){
            System.out.println(i);
        }





    }
}
