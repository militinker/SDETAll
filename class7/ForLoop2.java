package class7;

public class ForLoop2 {
    public static void main(String[] args) {

        for(int number=1;number<20;number++){
            if(number%2!=0)
            System.out.println(number);
        }

        for (int i = 1; i < 20; i+=2) {
            System.out.println(i);
        }
    }
}
