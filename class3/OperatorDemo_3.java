package class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number/3); //3
        System.out.println(number%3); //1

        System.out.println(16/5);
        System.out.println(16%5);

        System.out.println(156%2);
        System.out.println(155%2);

        System.out.println(1555%5); // to see if number is dividible by 5
        System.out.println(1555%2); // to see if its and even or odd number
    }
}
