package class3;

public class RealionalOperations_6 {
    public static void main(String[] args) {
      /*
      Arthmetic operators
      int + int =int
      int/int=int
      int-int=int
       */

      /*
      Relational Operators
      resaults are always true or false
      >
      >= greater or equal
      == equal to
      != not equal
      < less than
      <= less or equal
       */

        int number = 10;
        int number2 = 10;
        System.out.println(number + number2);
        boolean result = 20 > 10;
        System.out.println(result);

        System.out.println(20 > 10);  // is 20 greater than 10 - answer true
        System.out.println(5 > 10);  // is 5 greater than 10 - false
        System.out.println(5 != 10); // is 5 not equal to 10 - true
        System.out.println(5 == 10); // is 5 equal to 10 - false
        System.out.println(5 == 5); // is 5 equal to 5 - true
        System.out.println(10 >= 20); // is 10 greater or equal to 20 - false


    }
}