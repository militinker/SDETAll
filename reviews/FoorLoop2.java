public class FoorLoop2 {
    public static void main(String[] args) {

        for( int i=10; i>5; i++){
            System.out.println(i);
        }

        System.out.println("-------");

        for (int b=5; b<=1; b--){
            System.out.println("Happy");
        }


        for (int c=1; c<=6; c++){
            if (c%2==0){
                System.out.println("even");
            }
        }
    }
}
